// Simplified full-import row: only Name is required. School defaults to
// 'Independent' if blank. Email and Phone are optional.
// Everything else (login code/password, serial number, seat role, party,
// committee, constituency, state/city) is generated by the app.
interface StudentData {
  name: string;
  school?: string;
  email?: string;
  phone?: string;
  // scores-only mode fields
  serialNumber?: number;
  preeventScores?: number;
}

